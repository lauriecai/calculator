// ----- FUNCTIONS -----
// Function: Add
const add = function(a, b) {
    console.log(a + b);
}
add(5, 2);
add(42, 163);

// Function: Subtract
const subtract = function(a, b) {
    console.log(a - b);
}
subtract(5, 1);
subtract(1, 5);

// Function: Multiply
const multiply = function(a, b) {
    console.log(a * b);
}
multiply(3, 3);
multiply(4, 4);

// Function: Divide
const divide = function(a, b) {
    console.log(a / b);
}
divide(4, 2);
divide(3, 2);